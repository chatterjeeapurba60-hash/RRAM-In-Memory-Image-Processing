# ==============================
# RRAM FUNCTION
# ==============================
def rram_process(img, noise_level=0.05, alpha=1.2):
    G_min, G_max = 0.01, 1.0
    V = 0.2

    G = G_min + img * (G_max - G_min)

    noise = np.random.normal(0, noise_level * G)
    G_noisy = np.clip(G + noise, 0, None)

    G_nl = np.power(G_noisy, alpha)
    I = G_nl * V

    I_norm = (I - np.min(I)) / (np.max(I) - np.min(I) + 1e-8)

    return I_norm

