# ==============================
# CREATE SYNTHETIC DATASET (for ML graphs)
# ==============================
img_small = cv2.resize(img_gray, (32,32))

X = []
y = []

for i in range(200):
    noise = np.random.normal(0, 0.02, img_small.shape)
    sample = np.clip(img_small + noise, 0, 1)
    X.append(sample.flatten())
    y.append(i % 2)

X = np.array(X)
y = np.array(y)

# ==============================
# ACCURACY vs NOISE + ENERGY
# ==============================
noise_levels = [0, 0.05, 0.1, 0.2, 0.3]
acc_noise = []
energy_list = []

V = 0.2
G_min = 0.01
G_max = 1.0

for n in noise_levels:

    G = G_min + X * (G_max - G_min)

    if n == 0:
        noise = np.zeros_like(G)
    else:
        noise = np.random.normal(0, np.maximum(1e-9, n * G))

    G_noisy = np.clip(G + noise, 0, None) ** 1.2
    I = G_noisy * V

    energy = np.sum(V**2 * G_noisy)
    energy_list.append(energy)

    X_train, X_test, y_train, y_test = train_test_split(I, y, test_size=0.2)

    model_rf = RandomForestClassifier()
    model_rf.fit(X_train, y_train)

    y_pred = model_rf.predict(X_test)
    acc_noise.append(accuracy_score(y_test, y_pred))

# ==============================
# ACCURACY vs CONDUCTANCE LEVEL
# ==============================
levels = [2, 4, 8, 16, 32]
acc_levels = []

for L in levels:
    G = G_min + X * (G_max - G_min)

    G_quant = np.round(G * L) / L
    I = G_quant * V

    X_train, X_test, y_train, y_test = train_test_split(I, y, test_size=0.2)

    model_rf = RandomForestClassifier()
    model_rf.fit(X_train, y_train)

    y_pred = model_rf.predict(X_test)
    acc_levels.append(accuracy_score(y_test, y_pred))

